<h3 class = "fw-semibold text-secondary">Max Scheler</h3>

<div class="text-left">
<p>Um dos principais axiólogos do século XX. Sua principal obra se intitula Ética. Nela, Scheler descreve que o dever não é o que fundamenta o valor, mas, ao contrário, que o valor é que fundamenta o dever. Ou seja, ele defende que existe uma materialidade e objetividade nos valores, o que vai na contrariedade do formalismo kantiano. Segundo Scheler, os valores são conhecidos mediante a intuição, não sendo acessíveis à razão. Desse modo, temos que os valores, para Scheler: são qualidades ideais, isto é, não são reais; são objetivos, ou seja, dão-se independentemente de serem conhecidos; são transcendentes e materiais.</p>

